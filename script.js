document.getElementById("home").addEventListener("click", function () {
  var cardx = document.getElementById("cardy");
  if (cardx.style.visibility == "visible") {
    cardx.style.visibility = "hidden";
  } else {
    cardx.style.visibility = "visible";
  }
});

